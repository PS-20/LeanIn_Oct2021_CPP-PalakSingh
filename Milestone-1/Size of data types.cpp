#include<iostream>
using namespace std;
int main()
{
	cout<<"Size of integer : "<<sizeof(int)<<endl;
	cout<<"Size of float : "<<sizeof(float)<<endl;
	cout<<"Size of double : "<<sizeof(double)<<endl;
	cout<<"Size of char : "<<sizeof(char)<<endl;
	
	return 0;
}

/* OUTPUT
Size of integer : 4
Size of float : 4
Size of double : 8
Size of char : 1 */
